package com.greenfoxacademy.all.models.array;

public class PostArray {

  private String what;
  private int[] numbers;

  public PostArray() {
  }

  public String getWhat() {
    return what;
  }

  public void setWhat(String what) {
    this.what = what;
  }

  public int[] getNumbers() {
    return numbers;
  }

  public void setNumbers(int[] numbers) {
    this.numbers = numbers;
  }
}